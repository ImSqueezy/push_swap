#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	if (dst <= src)
		ft_memcpy(dst, src, len);
	else
		while (len-- > 0)
			ft_memset((dst + len), *(int *)(src + len), 1);
	return (dst);
}
