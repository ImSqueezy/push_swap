#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char		a;
	long long	num;

	num = n;
	if (num < 0)
	{
		ft_putchar_fd('-', fd);
		num *= -1;
	}
	if (num >= 0 && num <= 9)
	{
		a = num + 48;
		ft_putchar_fd(a, fd);
	}
	else
	{
		ft_putnbr_fd(num / 10, fd);
		ft_putnbr_fd(num % 10, fd);
	}
}
